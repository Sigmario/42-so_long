#include "libft.h"

int	*ft_nbrcpy(int *n1, int *n2)
{
	int	i;

	i = 0;
	while (n2[i])
	{
		n1[i] = n2[i];
		i++;
	}
	n1[i] = 0;
	return (n1);
}
